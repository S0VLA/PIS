// Этот скрипт отвечает за эффекты подсветки постов и анимации логотипа с использованием jQuery
$(document).ready(function() {
    // ===== Эффект подсветки поста при наведении =====
    // При наведении на элемент с классом .one-post плавно меняем прозрачность блока-тени
    $('.one-post').hover(
        function() {
            // Наведение курсора: увеличиваем прозрачность до 0.2 за 300 мс
            $(this).find('.one-post-shadow').stop().animate({opacity: 0.2}, 300);
        },
        function() {
            // Уход курсора: возвращаем прозрачность к 0 за 300 мс
            $(this).find('.one-post-shadow').stop().animate({opacity: 0}, 300);
        }
    );

    // ===== Эффект увеличения логотипа при наведении =====
    // Находим изображение логотипа (предполагается, что это единственное изображение в шапке)
    // Можно использовать селектор '.header img'
    $('.header img').hover(
        function() {
            // Наведение: увеличиваем ширину и высоту на 20px
            $(this).stop().animate({
                width: '+=20px',
                height: '+=20px'
            }, 200);
        },
        function() {
            // Уход: возвращаем к исходным размерам
            $(this).stop().animate({
                width: '-=20px',
                height: '-=20px'
            }, 200);
        }
    );
});